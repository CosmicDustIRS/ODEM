function x = GP_observation_factor( etime)
% This function adjusts the gas production to match the observations.
% Formula for the factor was done in excel. VALID FROM SEP 2014 to NOV 2015

logGP_according_to_observation = 9.8501629045*10^-45*etime^6 ...
                                -2.8329102281*10^-35*etime^5 ...
                                +3.3935726896*10^-26*etime^4 ...
                                -2.1673412482*10^-17*etime^3 ...
                                +7.7833739573*10^-9*etime^2 ...
                                -1.4902392153*etime ...
                                +1.1884542594*10^8;
                         
logGP_according_to_model = 6.7949012859*10^-44*etime^6 ...
                          -1.962472631*10^-34*etime^5 ...
                          +2.361091455*10^-25*etime^4 ...
                          -1.5146822286*10^-16*etime^3 ...
                          +5.4645498238*10^-8*etime^2 ...
                          -1.0512074313*10^1*etime ...
                          +8.4239253396*10^8;
                      
x = 10^logGP_according_to_observation / 10^logGP_according_to_model;

                            